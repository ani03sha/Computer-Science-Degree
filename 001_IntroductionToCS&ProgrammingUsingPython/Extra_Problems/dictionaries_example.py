def most_common_word(frequencies):
    best = max(frequencies.values())
    words = []
    for k in frequencies:
        if frequencies[k] == best:
            words.append(k)
    return (words, best)


def most_often(frequencies, minimumTimes):
    result = []
    flag = False
    while not flag:
        temp = most_common_word(frequencies)
        if temp[1] >= minimumTimes:
            result.append(temp)
            for w in temp[0]:
                del(frequencies[w])
        else:
            flag = True
    return result


